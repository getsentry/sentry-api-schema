/**
 * Pagination wrapper generator.
 *
 * Post-processes the @hey-api/openapi-ts output to emit a `pagination.gen.ts`
 * file containing typed pagination wrappers for every operation in the SDK
 * that accepts a `cursor` query parameter.
 *
 * Why a post-processor and not a Hey API plugin?
 *   The Hey API plugin API is documented as in-development and explicitly
 *   warns custom plugins may need rewriting on minor bumps (see
 *   https://heyapi.dev/openapi-ts/plugins/custom). This script is a stable,
 *   owned alternative that depends only on the *output shape* of the SDK
 *   (which is far more stable than the plugin API).
 *
 * What gets generated, per paginated operation `<fnName>`:
 *   - Always: `fetchPage_<fnName>(options, cursor?)` — single page with cursors
 *   - When response 200 is an array shape:
 *       `paginateAll_<fnName>(options, paginateOpts?)`
 *       `paginateUpTo_<fnName>(options, paginateOpts)`
 *
 * Inputs (read-only):
 *   - openapi-derefed.json — to detect which operations have `cursor`
 *   - src/sdk.gen.ts        — to extract function names and their `*Data` types
 *   - src/types.gen.ts      — to detect array-shaped vs compound responses
 *
 * Output:
 *   - src/pagination.gen.ts
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const SPEC_PATH = join(ROOT, "openapi-derefed.json");
const SDK_PATH = join(ROOT, "src", "sdk.gen.ts");
const TYPES_PATH = join(ROOT, "src", "types.gen.ts");
const OUT_PATH = join(ROOT, "src", "pagination.gen.ts");

const FILE_HEADER = `// This file is auto-generated by scripts/generate-pagination.mjs
// Do not edit directly. Re-run \`bun run build\` to regenerate.
//
// One typed wrapper trio (or just fetchPage, for non-array responses) is
// emitted per SDK operation whose OpenAPI definition accepts a \`cursor\`
// query parameter (Sentry's cursor-pagination signal). Each wrapper
// preserves the original operation's request types and inserts the
// cursor into \`query\` transparently via the \`_withCursor\` runtime
// helper in \`./sentry-pagination\`.
`;

const spec = JSON.parse(readFileSync(SPEC_PATH, "utf8"));
const sdkSource = readFileSync(SDK_PATH, "utf8");
const typesSource = readFileSync(TYPES_PATH, "utf8");

// =====================================================================
// 1. Detect paginated routes
// =====================================================================

/**
 * Allow-list of operations that the runtime API paginates via Link-header
 * cursors but whose OpenAPI spec is incomplete (no `cursor` parameter
 * declared). These get wrappers generated regardless of the spec.
 *
 * Each entry is a `${METHOD} ${path}` key. Add a justification comment
 * with each entry so future maintainers understand WHY the spec is being
 * overridden — and so entries can be removed once the upstream
 * `getsentry/sentry` schema is fixed.
 *
 * Verification: every entry should be confirmed against the live API
 * (response includes a `Link` header with `rel="next"; cursor="..."`)
 * before being added.
 */
const PAGINATED_BUT_UNMARKED = new Set([
  // /organizations/{org}/alert-rules/ — used by getsentry/cli's
  // listMetricAlertsPaginated. The runtime accepts `cursor` and `per_page`
  // (CLI proves it via raw apiRequestToRegion); the spec only declares the
  // path param. Remove this entry once the upstream OpenAPI spec is fixed.
  "GET /api/0/organizations/{organization_id_or_slug}/alert-rules/",
]);

/**
 * Build the set of paginated routes, keyed by `${METHOD} ${path}`. A route
 * qualifies if either:
 *   - its OpenAPI definition declares a `cursor` query parameter, OR
 *   - it appears in PAGINATED_BUT_UNMARKED.
 *
 * @hey-api's `defaultPaginationKeywords` (cursor/after/before/offset/page/start)
 * is wider than what we need. We restrict to `cursor` only because Sentry's
 * pagination model is uniformly cursor-based via Link headers — the other
 * keywords would produce wrappers our helpers can't drive.
 */
const paginatedRoutes = new Map();
for (const [path, methods] of Object.entries(spec.paths ?? {})) {
  for (const [method, op] of Object.entries(methods)) {
    if (typeof op !== "object" || op === null) continue;
    const key = `${method.toUpperCase()} ${path}`;
    const hasCursor =
      Array.isArray(op.parameters) &&
      op.parameters.some(
        (p) => p && p.name === "cursor" && p.in === "query",
      );
    if (hasCursor || PAGINATED_BUT_UNMARKED.has(key)) {
      paginatedRoutes.set(key, op);
    }
  }
}

// Sanity check the allow-list — warn loudly if an entry doesn't match
// any operation (typo or upstream rename).
for (const key of PAGINATED_BUT_UNMARKED) {
  if (!paginatedRoutes.has(key)) {
    console.warn(
      `generate-pagination: PAGINATED_BUT_UNMARKED entry "${key}" did not\n` +
        `  match any operation in openapi-derefed.json. Either the path was\n` +
        `  renamed upstream or the entry is misspelled.`,
    );
  }
}

// =====================================================================
// 2. Parse the generated SDK source
// =====================================================================

/**
 * Parse `src/sdk.gen.ts` to extract, for each exported function:
 *   - fn:        function identifier (e.g. `listAnOrganization_sIssues`)
 *   - typeBase:  type name root (e.g. `ListAnOrganizationSissues`)
 *   - method:    HTTP method (`get`, `post`, ...)
 *   - url:       URL template
 *
 * Hey API's emit shape is stable; we split on `^export const ` and parse
 * each block with anchored regexes:
 *
 *     export const <fn> = <ThrowOnError extends boolean = false>(options...
 *         Options<<TypeBase>Data, ThrowOnError>) => (options.client ?? client)
 *         .<method><<TypeBase>Responses, <TypeBase>Errors, ThrowOnError>({
 *             url: '<url>',
 *             ...options
 *         });
 */
const sdkOps = [];
for (const block of sdkSource.split(/^export const /m).slice(1)) {
  const fnMatch = block.match(/^(\w+) =/);
  const typeMatch = block.match(/Options<(\w+)Data,/);
  const urlMatch = block.match(/url: '([^']+)'/);
  const methodMatch = block.match(/\)\.(\w+)</);
  if (!fnMatch || !typeMatch || !urlMatch || !methodMatch) continue;
  sdkOps.push({
    fn: fnMatch[1],
    typeBase: typeMatch[1],
    url: urlMatch[1],
    method: methodMatch[1].toLowerCase(),
  });
}

// =====================================================================
// 3. Detect array-shaped responses
// =====================================================================

/**
 * Detect whether an operation's success-200 response is a flat `Array<...>`
 * shape. We inspect `src/types.gen.ts` directly — much cheaper than `tsc`.
 *
 * The generated type looks like:
 *     export type <TypeBase>Responses = {
 *         /** optional JSDoc *\/
 *         200: Array<{ ... }>;
 *         /** ... *\/
 *         404: ...;
 *     };
 *
 * We find the first `<indent>200:` line and inspect its first non-whitespace
 * character: `A` for `Array<`, anything else for compound.
 */
const isArrayResponse = (typeBase) => {
  const start = typesSource.indexOf(
    `export type ${typeBase}Responses = {`,
  );
  if (start < 0) return false;
  const m = typesSource.slice(start).match(/^\s*200:\s*([A-Za-z<{])/m);
  return !!m && m[1] === "A";
};

// =====================================================================
// 4. Cross-reference SDK ops with paginated routes
// =====================================================================

const targets = sdkOps
  .filter((op) =>
    paginatedRoutes.has(`${op.method.toUpperCase()} ${op.url}`),
  )
  .map((op) => ({ ...op, isArray: isArrayResponse(op.typeBase) }))
  .sort((a, b) => a.fn.localeCompare(b.fn));

if (targets.length === 0) {
  console.warn(
    "generate-pagination: no paginated operations found. Check that the\n" +
      "  SDK has been generated (src/sdk.gen.ts) and the spec contains\n" +
      "  operations with a `cursor` query parameter.",
  );
}

// =====================================================================
// 5. Emit the file
// =====================================================================

/**
 * Each wrapper accepts the underlying SDK function's `Options<TData>` shape
 * (with `query.cursor` Omit-ted, see `PaginationQuery<TQuery>` below) and
 * delegates to the generic runtime helper (`fetchPage` / `paginateAll` /
 * `paginateUpTo` from `./sentry-pagination`).
 *
 * Type-system contract:
 *   - User-facing query type strips `cursor` (helper-managed) and widens
 *     with `per_page?: number` (runtime-supported on every paginated route,
 *     even when the spec omits it).
 *   - SDK call uses the `_withCursor` runtime helper to merge in the
 *     managed cursor and reshape back to `Options<TData>`. The cast chain
 *     lives once in `_withCursor`, not per-wrapper.
 *   - Return type cast `as unknown as Promise<{...success}>` aligns the
 *     SDK's distributed `RequestResult<…>` discriminated union with our
 *     local `SdkResult<TData, TError>` shape. Runtime is correct because
 *     `unwrapResult` checks `error !== undefined`.
 *
 * Why `Omit<…, 'cursor'>`? Accepting a cursor in `query` would silently
 * shadow the helper-managed value. Surfacing a clear "use the helper's
 * cursor argument" type error is far safer.
 *
 * Why widen with `per_page`? Sentry's pagination framework accepts it on
 * every cursor-paginated route at runtime, but the OpenAPI spec only
 * declares it on a few. Optional widening lets callers pass it without an
 * `as` cast while keeping documented params strongly typed.
 */

const userOptionsType = (op) =>
  `Omit<Options<${op.typeBase}Data>, 'query'> & { query?: PaginationQuery<${op.typeBase}Data['query']> }`;

const item200 = (op) => `${op.typeBase}Responses[200]`;

/**
 * Emit the SDK call expression used by all three wrapper kinds.
 *
 * The cursor-merge cast is hoisted into the `_withCursor` runtime helper
 * (see lib/sentry-pagination.ts). The remaining `as unknown as Promise<…>`
 * cast aligns the SDK's distributed `RequestResult<…>` with our local
 * `SdkResult<…>` discriminated union — must stay per-wrapper because the
 * `data` and `Promise` types are operation-specific.
 */
const sdkCallExpr = (op, cursorIdent) =>
  `${op.fn}(_withCursor<Options<${op.typeBase}Data>>(options, ${cursorIdent})) as unknown as Promise<{ data: ${item200(op)}; error: undefined; request: Request; response: Response }>`;

const emitFetchPage = (op) => [
  `/** Fetch a single page of \`${op.fn}\` with cursors from the Link header. */`,
  `export const fetchPage_${op.fn} = (`,
  `    options: ${userOptionsType(op)},`,
  `    cursor?: string,`,
  `): Promise<PaginatedResponse<${item200(op)}>> =>`,
  `    fetchPage<${item200(op)}>(`,
  `        (c) => ${sdkCallExpr(op, "c")},`,
  `        '${op.fn}',`,
  `        cursor,`,
  `    );`,
  ``,
];

const emitPaginateAll = (op) => [
  `/** Eagerly fetch all pages of \`${op.fn}\`. Bounded by \`maxPages\` (default 50). */`,
  `export const paginateAll_${op.fn} = (`,
  `    options: ${userOptionsType(op)},`,
  `    paginateOptions?: PaginateAllOptions,`,
  `): Promise<${item200(op)}> =>`,
  `    paginateAll<${item200(op)}[number]>(`,
  `        (c) => ${sdkCallExpr(op, "c")},`,
  `        '${op.fn}',`,
  `        paginateOptions,`,
  `    ) as Promise<${item200(op)}>;`,
  ``,
];

const emitPaginateUpTo = (op) => [
  `/** Paginate \`${op.fn}\` up to \`limit\` items; see PaginateUpToOptions. */`,
  `export const paginateUpTo_${op.fn} = (`,
  `    options: ${userOptionsType(op)},`,
  `    paginateOptions: PaginateUpToOptions,`,
  `): Promise<{ data: ${item200(op)}; nextCursor?: string }> =>`,
  `    paginateUpTo<${item200(op)}[number]>(`,
  `        (c) => ${sdkCallExpr(op, "c")},`,
  `        paginateOptions,`,
  `        '${op.fn}',`,
  `    ) as Promise<{ data: ${item200(op)}; nextCursor?: string }>;`,
  ``,
];

/**
 * Build the imports block. Single pass over `targets` populates two
 * sorted lists: SDK function names (camelCase, value imports) and type
 * names (PascalCase, type-only imports).
 */
const sdkFnNames = targets.map((op) => op.fn);
const typeNames = new Set();
for (const op of targets) {
  typeNames.add(`${op.typeBase}Data`);
  typeNames.add(`${op.typeBase}Responses`);
}

const lines = [
  FILE_HEADER,
  ``,
  `import type { Options } from './sdk.gen';`,
  `import {`,
  ...sdkFnNames.map((n) => `    ${n},`),
  `} from './sdk.gen';`,
  `import type {`,
  ...[...typeNames].sort().map((t) => `    ${t},`),
  `} from './types.gen';`,
  `import {`,
  `    _withCursor,`,
  `    fetchPage,`,
  `    paginateAll,`,
  `    paginateUpTo,`,
  `    type PaginateAllOptions,`,
  `    type PaginateUpToOptions,`,
  `    type PaginatedResponse,`,
  `} from './sentry-pagination';`,
  ``,
  `/**`,
  ` * Strip \`cursor\` from a query type (the wrapper manages the cursor)`,
  ` * and widen with \`per_page\`, which Sentry's API accepts on every`,
  ` * cursor-paginated route at runtime even when the OpenAPI spec`,
  ` * omits it. The widening is opt-in (the field is optional), so`,
  ` * documented query params remain strongly typed and undocumented`,
  ` * \`per_page\` becomes addressable without forcing every caller into`,
  ` * an \`as\` cast.`,
  ` */`,
  `type PaginationQuery<TQuery> = TQuery extends undefined`,
  `    ? { per_page?: number }`,
  `    : Omit<NonNullable<TQuery>, 'cursor'> & { per_page?: number };`,
  ``,
];

for (const op of targets) {
  lines.push(`// ${op.fn} — ${op.method.toUpperCase()} ${op.url} (${op.isArray ? "array" : "compound"})`);
  lines.push(...emitFetchPage(op));
  if (op.isArray) {
    lines.push(...emitPaginateAll(op));
    lines.push(...emitPaginateUpTo(op));
  }
}

writeFileSync(OUT_PATH, lines.join("\n"));

const arrayCount = targets.filter((t) => t.isArray).length;
const nonArrayCount = targets.length - arrayCount;
const exportCount = arrayCount * 3 + nonArrayCount;
console.log(
  `generate-pagination: wrote ${OUT_PATH}\n` +
    `  ${targets.length} paginated operations (${arrayCount} array-shaped, ${nonArrayCount} compound)\n` +
    `  ${exportCount} exports total`,
);
