/**
 * Pagination wrapper generator.
 *
 * Post-processes the @hey-api/openapi-ts output to emit a `pagination.gen.ts`
 * file containing typed pagination wrappers for every operation in the SDK
 * that accepts a `cursor` query parameter.
 *
 * Why a post-processor and not a Hey API plugin?
 *   The Hey API plugin API is documented as in-development and explicitly
 *   warns custom plugins may need rewriting on minor bumps (see
 *   https://heyapi.dev/openapi-ts/plugins/custom). This script is a stable,
 *   owned alternative that depends only on the *output shape* of the SDK
 *   (which is far more stable than the plugin API).
 *
 * What gets generated, per paginated operation `<fnName>`:
 *   - Always: `fetchPage_<fnName>(options, cursor?)` — single page with cursors
 *   - When response 200 is an array shape:
 *       `paginateAll_<fnName>(options, paginateOpts?)`
 *       `paginateUpTo_<fnName>(options, paginateOpts)`
 *
 * Inputs (read-only):
 *   - openapi-derefed.json — to detect which operations have `cursor`
 *   - src/sdk.gen.ts        — to extract function names and their `*Data` types
 *   - src/types.gen.ts      — to detect array-shaped vs compound responses
 *
 * Output:
 *   - src/pagination.gen.ts
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const SPEC_PATH = join(ROOT, "openapi-derefed.json");
const SDK_PATH = join(ROOT, "src", "sdk.gen.ts");
const TYPES_PATH = join(ROOT, "src", "types.gen.ts");
const OUT_PATH = join(ROOT, "src", "pagination.gen.ts");

const FILE_HEADER = `// This file is auto-generated by scripts/generate-pagination.mjs
// Do not edit directly. Re-run \`bun run build\` to regenerate.
//
// One typed wrapper trio (or just fetchPage, for non-array responses) is
// emitted per SDK operation whose OpenAPI definition accepts a \`cursor\`
// query parameter (Sentry's cursor-pagination signal). Each wrapper
// preserves the original operation's request types and inserts the
// cursor into \`query\` transparently.
`;

const spec = JSON.parse(readFileSync(SPEC_PATH, "utf8"));
const sdkSource = readFileSync(SDK_PATH, "utf8");
const typesSource = readFileSync(TYPES_PATH, "utf8");

/**
 * Extract the set of operations that accept a `cursor` query parameter,
 * keyed by `${METHOD} ${path}`. Sentry's API uses cursor-based pagination
 * via Link headers; the presence of `cursor` is our paginated-operation
 * signal.
 *
 * @hey-api's built-in pagination detection uses `defaultPaginationKeywords`
 * (`after`, `before`, `cursor`, `offset`, `page`, `start`). We restrict to
 * `cursor` only because that's what Sentry's spec actually uses for cursor
 * pagination — the others would generate wrappers for operations whose
 * pagination model is something else (offset, etc.) and our helpers
 * wouldn't match. If the spec ever introduces non-cursor paginated
 * endpoints, this is the place to extend.
 */
const paginatedRoutes = new Map();
for (const [path, methods] of Object.entries(spec.paths ?? {})) {
  for (const [method, op] of Object.entries(methods)) {
    if (typeof op !== "object" || op === null) continue;
    const params = op.parameters;
    if (!Array.isArray(params)) continue;
    const hasCursor = params.some(
      (p) => p && p.name === "cursor" && p.in === "query",
    );
    if (hasCursor) {
      paginatedRoutes.set(`${method.toUpperCase()} ${path}`, op);
    }
  }
}

/**
 * Parse `src/sdk.gen.ts` to extract, for each exported function:
 *   - fn:        function identifier (e.g. `listAnOrganization_sIssues`)
 *   - typeBase:  type name root (e.g. `ListAnOrganizationSissues` — the
 *                generator suffixes `Data`/`Errors`/`Responses`)
 *   - method:    HTTP method (`get`, `post`, ...)
 *   - url:       URL template (e.g. `/api/0/organizations/{...}/issues/`)
 *
 * Hey API emits each operation in a known-stable shape:
 *
 *     export const <fn> = <ThrowOnError extends boolean = false>(options...
 *         Options<<TypeBase>Data, ThrowOnError>) => (options.client ?? client)
 *         .<method><<TypeBase>Responses, <TypeBase>Errors, ThrowOnError>({
 *             url: '<url>',
 *             ...options
 *         });
 *
 * We split on `^export const ` and parse each block.
 */
const sdkOps = [];
const blocks = sdkSource.split(/^export const /m).slice(1);
for (const block of blocks) {
  const fnMatch = block.match(/^(\w+) =/);
  const typeMatch = block.match(/Options<(\w+)Data,/);
  const urlMatch = block.match(/url: '([^']+)'/);
  const methodMatch = block.match(/\)\.(\w+)</);
  if (!fnMatch || !typeMatch || !urlMatch || !methodMatch) continue;
  sdkOps.push({
    fn: fnMatch[1],
    typeBase: typeMatch[1],
    url: urlMatch[1],
    method: methodMatch[1].toLowerCase(),
  });
}

/**
 * Detect whether an operation's success-200 response is a flat `Array<...>`
 * shape. We inspect the source of `src/types.gen.ts` directly — much
 * cheaper and more deterministic than running `tsc`.
 *
 * The generated type looks like:
 *     export type <TypeBase>Responses = {
 *         /** optional JSDoc *\/
 *         200: Array<{ ... }>;
 *         /** ... *\/
 *         404: ...;
 *     };
 *
 * We find the first `<indent>200:` line within the Responses block and
 * inspect its first non-whitespace character: `A` for `Array<`, `{` for
 * a compound object shape, anything else (e.g. `string`, `number`) is
 * treated as non-array.
 */
const isArrayResponse = (typeBase) => {
  const marker = `export type ${typeBase}Responses = {`;
  const start = typesSource.indexOf(marker);
  if (start < 0) return false;
  const body = typesSource.slice(start);
  const m = body.match(/^\s*200:\s*([A-Za-z<{])/m);
  if (!m) return false;
  return m[1] === "A"; // `Array<...>`
};

/** Cross-reference the SDK ops with the paginated routes. */
const targets = sdkOps
  .filter((op) => paginatedRoutes.has(`${op.method.toUpperCase()} ${op.url}`))
  .map((op) => ({ ...op, isArray: isArrayResponse(op.typeBase) }))
  .sort((a, b) => a.fn.localeCompare(b.fn));

if (targets.length === 0) {
  console.warn(
    "generate-pagination: no paginated operations found. Check that\n" +
      "  the SDK has been generated (src/sdk.gen.ts) and the spec contains\n" +
      "  operations with a `cursor` query parameter.",
  );
}

/**
 * Build the `pagination.gen.ts` source string.
 *
 * Strategy: each wrapper takes the full `Options<...Data>` shape the SDK
 * function accepts, plus a managed `cursor` argument (or `paginateOpts`
 * for the all-pages / bounded variants). The wrapper merges `cursor` into
 * `options.query` and forwards to the SDK function. All runtime helpers
 * come from `./sentry-pagination.js`.
 *
 * Why `Omit<..., 'cursor'>` on the user-facing query type?
 *   The wrapper *manages* the cursor — accepting one in `query` would
 *   silently shadow the helper-managed value. Surfacing a clear "you
 *   can't pass cursor here, use the helper's cursor argument" is far
 *   safer than the silent-shadow bug.
 */
const lines = [];
lines.push(FILE_HEADER);
lines.push("");
lines.push(`import type { Options } from './sdk.gen';`);
lines.push(`import {`);
for (const op of targets) {
  lines.push(`    ${op.fn},`);
}
lines.push(`} from './sdk.gen';`);
lines.push(`import type {`);
const typeNames = new Set();
for (const op of targets) {
  typeNames.add(`${op.typeBase}Data`);
  typeNames.add(`${op.typeBase}Responses`);
}
for (const t of [...typeNames].sort()) {
  lines.push(`    ${t},`);
}
lines.push(`} from './types.gen';`);
lines.push(
  `import {`,
  `    fetchPage,`,
  `    paginateAll,`,
  `    paginateUpTo,`,
  `    type PaginateAllOptions,`,
  `    type PaginateUpToOptions,`,
  `    type PaginatedResponse,`,
  `} from './sentry-pagination';`,
);
lines.push("");
lines.push(
  `/**`,
  ` * Strip \`cursor\` from a query type (the wrapper manages the cursor).`,
  ` */`,
  `type WithoutCursor<TQuery> = TQuery extends undefined`,
  `    ? undefined`,
  `    : Omit<NonNullable<TQuery>, 'cursor'>;`,
  ``,
);

for (const op of targets) {
  const dataType = `${op.typeBase}Data`;
  const responsesType = `${op.typeBase}Responses`;
  const item200Type = `${responsesType}[200]`;
  const optionsType = `Options<${dataType}>`;
  const userOptionsType = `Omit<${optionsType}, 'query'> & { query?: WithoutCursor<${dataType}['query']> }`;

  // Helper to construct the SDK call expression — shared across the trio.
  //
  // The cast chain `as unknown as Promise<{ data; error: undefined; ... }>`
  // is a deliberate compromise:
  //   1. The user-facing `options` type has `query.cursor` Omit-ted, so we
  //      can't directly assign back to the SDK's `Options<...Data>` shape.
  //   2. The SDK's `RequestResult` return type distributes over the
  //      defaulted `ThrowOnError extends boolean = boolean` generic, which
  //      collapses unpredictably and doesn't structurally match our
  //      `SdkResult<TData, TError>` discriminated union (defined locally
  //      in `lib/sentry-pagination.ts` to keep that module zero-dep).
  // The runtime path is correct: `unwrapResult` inside the helpers handles
  // both `{ error: undefined }` and `{ error: ... }` cases via a check on
  // `result.error`. The cast is purely structural-typing glue.
  const sdkCallExpr = (cursorIdent) => [
    `            ${op.fn}({`,
    `                ...options,`,
    `                query: { ...(options.query as Record<string, unknown> | undefined), cursor: ${cursorIdent} } as ${dataType}['query'],`,
    `            } as unknown as ${optionsType}) as unknown as Promise<{ data: ${item200Type}; error: undefined; request: Request; response: Response }>,`,
  ];

  lines.push(
    `// ============================================================`,
  );
  lines.push(`// ${op.fn}`);
  lines.push(`// ${op.method.toUpperCase()} ${op.url}`);
  lines.push(`// response shape: ${op.isArray ? "Array<...>" : "compound (only fetchPage is generated)"}`);
  lines.push(`// ============================================================`);
  lines.push("");

  // fetchPage — always emitted; works for any response shape
  lines.push(`/**`);
  lines.push(` * Fetch a single page of \`${op.fn}\` and return the data plus`);
  lines.push(` * pagination cursors parsed from the Link header.`);
  lines.push(` */`);
  lines.push(`export const fetchPage_${op.fn} = (`);
  lines.push(`    options: ${userOptionsType},`);
  lines.push(`    cursor?: string,`);
  lines.push(`): Promise<PaginatedResponse<${item200Type}>> =>`);
  lines.push(`    fetchPage<${item200Type}>(`);
  lines.push(`        (c) =>`);
  lines.push(...sdkCallExpr("c"));
  lines.push(`        '${op.fn}',`);
  lines.push(`        cursor,`);
  lines.push(`    );`);
  lines.push("");

  if (!op.isArray) continue;

  // For array-shaped responses, item200Type is `Array<TItem>`. The
  // helpers signal "I need an Array<TItem>" via their TItem generic.
  // We forward item200Type directly as the array type.
  lines.push(`/**`);
  lines.push(` * Eagerly fetch all pages of \`${op.fn}\` and concatenate.`);
  lines.push(` * Bounded by \`maxPages\` (default 50) for safety.`);
  lines.push(` */`);
  lines.push(`export const paginateAll_${op.fn} = (`);
  lines.push(`    options: ${userOptionsType},`);
  lines.push(`    paginateOptions?: PaginateAllOptions,`);
  lines.push(`): Promise<${item200Type}> =>`);
  lines.push(`    paginateAll<${item200Type}[number]>(`);
  lines.push(`        (c) =>`);
  lines.push(...sdkCallExpr("c"));
  lines.push(`        '${op.fn}',`);
  lines.push(`        paginateOptions,`);
  lines.push(`    ) as Promise<${item200Type}>;`);
  lines.push("");

  lines.push(`/**`);
  lines.push(` * Paginate \`${op.fn}\` up to a hard \`limit\`, suppressing the`);
  lines.push(` * next-cursor when the last page is trimmed.`);
  lines.push(` */`);
  lines.push(`export const paginateUpTo_${op.fn} = (`);
  lines.push(`    options: ${userOptionsType},`);
  lines.push(`    paginateOptions: PaginateUpToOptions,`);
  lines.push(`): Promise<{ data: ${item200Type}; nextCursor?: string }> =>`);
  lines.push(`    paginateUpTo<${item200Type}[number]>(`);
  lines.push(`        (c) =>`);
  lines.push(...sdkCallExpr("c"));
  lines.push(`        paginateOptions,`);
  lines.push(`        '${op.fn}',`);
  lines.push(`    ) as Promise<{ data: ${item200Type}; nextCursor?: string }>;`);
  lines.push("");
}

const output = lines.join("\n");
writeFileSync(OUT_PATH, output);

const arrayCount = targets.filter((t) => t.isArray).length;
const nonArrayCount = targets.length - arrayCount;
const exportCount = arrayCount * 3 + nonArrayCount;
console.log(
  `generate-pagination: wrote ${OUT_PATH}\n` +
    `  ${targets.length} paginated operations (${arrayCount} array-shaped, ${nonArrayCount} compound)\n` +
    `  ${exportCount} exports total`,
);
